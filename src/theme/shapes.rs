//! Скругления углов — аналог Shapes из Material Design 3.
//!
//! Определяет радиусы скругления для UI-компонентов.

/// Набор радиусов скругления.
///
/// Используется виджетами для единообразного скругления углов.
#[derive(Clone, Debug)]
pub struct Shapes {
    /// Малое скругление (4px) — кнопки, чипы.
    pub small: f32,
    /// Среднее скругление (8px) — карточки, диалоги.
    pub medium: f32,
    /// Большое скругление (16px) — модальные окна, нижние листы.
    pub large: f32,
}
