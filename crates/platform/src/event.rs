//! Типы событий платформы.

/// Событие платформы.
///
/// Обобщённое представление событий lifecycle и окна для любой платформы.
///
/// - `W` — тип окна платформы
/// - `I` — тип события ввода платформы
pub enum PlatformEvent<W, I> {
    /// Окно инициализировано (или пересоздано).
    InitWindow(W),

    /// Событие ввода (касание, клавиатура и т.д.).
    Input(I),

    /// Приложение возобновило работу.
    Resume,

    /// Приложение приостановлено.
    Pause,

    /// Приложение остановлено.
    Stop,

    /// Приложение уничтожено.
    Destroy,
}
